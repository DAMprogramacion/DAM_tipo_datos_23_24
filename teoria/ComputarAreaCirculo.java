/* Programa que calcula el área
 * de un círculo, se declara las variables
 * posteriormente se inicializan
 * se muestra el resultado */
 
public class ComputarAreaCirculo {
	
	public static void main (String[] args) {
		
		int radio;
		double area;
		radio = 57;
		area = radio * radio * 3.1416;
		System.out.println("El área de un círculo de radio " + radio + " vale " + area);
		
	}
}

